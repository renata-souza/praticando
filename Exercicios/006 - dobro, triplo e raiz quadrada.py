# Crie um programa que receba um número inteiro e mostre na tela o seu dobro, triplo e sua raíz quadrada.

n = int(input())
dobro = n * 2 
triplo = n * 3
raiz = n ** (0.5)

print(f'O dobro de {n} é: {dobro}')
print(f'O triplo de {n} é: {triplo}')
print(f'A raíz quadrada de {n} é: {raiz}')
